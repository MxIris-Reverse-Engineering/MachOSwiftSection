import Foundation

extension Int32 {
    var int: Int {
        return Int(self)
    }
    
    var uint: UInt {
        return UInt(self)
    }
    
    var uint64: UInt64 {
        return UInt64(self)
    }
    
    var uint32: UInt32 {
        return UInt32(self)
    }
    
    var uint16: UInt16 {
        return UInt16(self)
    }
    
    var int64: Int64 {
        return Int64(self)
    }
}

